/**
 * Шаг округления калорий НА ОТОБРАЖЕНИИ. В БД храним точное значение.
 * Округление снижает обсессивную сверку точных цифр (Eikey 2021 cross-source
 * check loop). Конкретный шаг эмпирически не установлен — дефолт 25, пилот.
 */
export const CALORIE_ROUNDING = 25;

/** Округлить калории для показа пользователю. */
export function roundCalories(kcal: number, step = CALORIE_ROUNDING): number {
  return Math.round(kcal / step) * step;
}

