// SAFETY-1 РЕШЕНО (диетолог, 2026-06-16): фиксированные минимумы (1500/1800)
// упразднены. Индивидуальный защитный минимум = BMR пользователя (Mifflin-St
// Jeor). BMR уже учитывает пол/возраст/рост/вес, поэтому не бывает «слишком
// высоким для одних и низким для других», как любая константа. Сам floor живёт
// в lib/zone.ts (floor = BMR); отдельной константы здесь больше нет.
//
// При текущих коэффициентах активности (мин. 1.2) нижняя граница зоны
// (TDEE−15% = BMR·1.2·0.85 = BMR·1.02) всегда ВЫШЕ BMR, поэтому floor=BMR
// фактически почти не срабатывает и работает как гарантия для краевых случаев.

/**
 * Скрытый safety-флаг (SAFETY-1, вопрос 4): если 7-дневное среднее потребление
 * ниже этой доли BMR — событие фиксируется в аналитике (metrics.ts). Никаких
 * пользовательских предупреждений/цветов/баннеров — продукт молчит (принцип 5).
 * Порог 0.75 задан диетологом 2026-06-16; калибровка — после данных пилота.
 */
export const SAFETY_FLAG_BMR_FRACTION = 0.75;

/**
 * Порог GAD-2 (слой 1 скрининга). cutoff ≥3.
 * SAFETY-2 РЕШЕНО (2026-06-16): порог ≥3 подтверждён (лучший баланс
 * чувствительности/специфичности). Трактовка результата — НЕ «у тебя
 * расстройство», а «может зайти режим без чисел» (срабатывание = персонализация
 * опыта, не диагноз). Это уже отражено в реакции на триггер (мягко, без блока).
 */
export const GAD2_CUTOFF = 3;

/**
 * Слой 2 (скрытые поведенческие сигналы). SAFETY-2 РЕШЕНО: слой 2 остаётся
 * ТОЛЬКО аналитическим — никаких решений по нему до накопления данных пилота.
 *
 * Набор сигналов GOAL-FREE (психолог 2026-06-16): продукт сознательно НЕ
 * собирает цель по весу / скорость похудения (SPEC принцип 13 + «не за
 * похудение»), поэтому сигналы вроде «темп похудения >1%/нед» отвергнуты.
 * Финальный собираемый набор (всё — только аналитика, без решений):
 *  - BMI < SCREENING_BMI_SIGNAL (18.5) и < SCREENING_BMI_SIGNAL_STRONG (17.5) —
 *    производно из роста+веса в metrics.ts;
 *  - GAD-2 ≥ cutoff и «высокий балл по еде» — производные флаги в
 *    ScreeningResult (gad2AtCutoff / foodConcernHigh; см. lib/screening.ts).
 *    Сырые баллы НЕ храним. Наиболее ценный сигнал по психологу — foodConcernHigh;
 *  - добровольное использование soft-режима — уже в metrics.ts (everUsedSoft + switches).
 */
export const LAYER2_PLACEHOLDER = {
  // Намеренно пусто: слой 2 не выносит решений (см. lib/screening.ts).
  enabled: false,
} as const;

/**
 * BMI-сигнал слоя 2 (SAFETY-2). НЕ триггер решения — только фиксируется в
 * аналитике (metrics.ts). Порог 18.5 выбран специалистом сознательно «мягким»:
 * более агрессивные пороги дают много false-positive у людей небольшого роста /
 * естественно низкой массы в массовой аудитории.
 */
export const SCREENING_BMI_SIGNAL = 18.5;
// Более сильный BMI-сигнал (психолог 2026-06-16, дополнение к слою 2). Тоже
// только аналитика, НЕ триггер решения.
export const SCREENING_BMI_SIGNAL_STRONG = 17.5;

/**
 * SAFETY-4 РЕШЕНО (диетолог, 2026-06-16): recovery-ветка формулы зоны.
 * При BMI < RECOVERY_BMI_THRESHOLD (определение недовеса по ВОЗ) зона НЕ
 * рекомендует дефицит: нижняя граница = TDEE, верхняя = TDEE×(1+RECOVERY_SURPLUS).
 *
 * Полностью НЕВИДИМО — никаких уведомлений о недовесе/восстановлении/риске,
 * бейджей или смены копи (как защитный минимум; принцип «никогда о теле»). UI
 * показывает обычный диапазон без объяснений. Срабатывает только при РЕАЛЬНОМ
 * весе: при пропущенном весе берётся proxy (BMI 21.7) → recovery не включается,
 * т.к. без веса недовес оценить нельзя. См. lib/zone.ts.
 *
 * 18.5 здесь — порог формулы (триггер recovery); совпадает по значению с
 * SCREENING_BMI_SIGNAL, но это другая по смыслу величина (аналитический сигнал).
 */
export const RECOVERY_BMI_THRESHOLD = 18.5;
export const RECOVERY_SURPLUS = 0.1; // верхняя граница = TDEE +10% (консерв. альт. 5%)

